#include "Blinkt.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
// CONSTRUCTORS/DESTRUCTOR
////////////////////////////////////////////////////////////////////////////////////////////////////
Blinkt::Blinkt(uint8_t brightness)
: _controller(FastLED.addLeds<APA102, DATA_PIN, CLOCK_PIN, BGR>(_pixels, NUM_LEDS))
, _brightness(brightness)
{
}



////////////////////////////////////////////////////////////////////////////////////////////////////
// METHODS
////////////////////////////////////////////////////////////////////////////////////////////////////
CRGB(&Blinkt::Pixels())[NUM_LEDS]
{
  return _pixels;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void Blinkt::SetBrightness(uint8_t brightness)
{
  _brightness = brightness;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
void Blinkt::Show()
{
  _controller.showLeds(_brightness);
  _controller.showLeds(_brightness); //Have to call this twice for some reason
}
